"""Rule schemas: composable functors from CategorySystem to RuleSystem.

A ``RuleSchema`` is a functor that maps a ``CategorySystem`` to a
``RuleSystem``. Schemas are the abstract building blocks of grammar
formalisms: each schema defines a structural rule pattern, and
concrete rule instances are generated by instantiating the pattern
over a finite category inventory.

The two atomic schema types are:

- ``BinaryRuleSchema`` — defined by a match predicate on category
  pairs: ``match(left, right) -> result | None``.
- ``UnaryRuleSchema`` — defined by a match predicate on single
  categories: ``match(cat, system) -> list[result]``.

Schemas compose via ``|`` (union) and can carry default weights
via ``.weighted(w)``. Grammar formalisms are specific compositions::

    CCG = EVALUATION | HARMONIC_COMPOSITION | CROSSED_COMPOSITION
    LAMBEK = EVALUATION | ADJUNCTION_UNITS | TENSOR_INTRODUCTION | TENSOR_PROJECTION

The 13 bundled schemas (EVALUATION, HARMONIC_COMPOSITION, etc.)
are themselves composed from ~20 atomic schemas (ForwardApplication,
BackwardApplication, etc.), giving fine-grained control::

    # only forward application, no backward
    MY_GRAMMAR = ForwardApplication() | ForwardComposition()
"""

from __future__ import annotations

from abc import ABC, abstractmethod
from collections.abc import Iterator

from quivers.stochastic.categories import (
    Category,
    CategorySystem,
    ModalCategory,
    ProductCategory,
    SlashCategory,
    UnitCategory,
)
from quivers.stochastic._rule_system import RuleSystem


# ================================================================
# abstract base
# ================================================================

class RuleSchema(ABC):
    """A functor CategorySystem -> RuleSystem.

    Schemas are the abstract specification of structural rules.
    They compose via ``|`` (union) and ``+`` (alias for ``|``).
    """

    @abstractmethod
    def __call__(self, system: CategorySystem) -> RuleSystem:
        """Instantiate this schema over a category system.

        Parameters
        ----------
        system : CategorySystem
            The finite category inventory.

        Returns
        -------
        RuleSystem
            Concrete rules for this schema.
        """
        ...

    def __or__(self, other: RuleSchema) -> RuleSchema:
        """Union of two schemas."""
        return UnionSchema(self, other)

    def __add__(self, other: RuleSchema) -> RuleSchema:
        """Alias for union."""
        return self | other

    def weighted(self, weight: float) -> RuleSchema:
        """Attach a default weight to all rules from this schema."""
        return WeightedSchema(self, weight)


class UnionSchema(RuleSchema):
    """Union of two schemas (composes their rule systems via +)."""

    def __init__(self, left: RuleSchema, right: RuleSchema) -> None:
        self._left = left
        self._right = right

    def __call__(self, system: CategorySystem) -> RuleSystem:
        return self._left(system) + self._right(system)

    def __repr__(self) -> str:
        return f"({self._left!r} | {self._right!r})"


class WeightedSchema(RuleSchema):
    """Schema wrapper that attaches a default weight to all rules."""

    def __init__(self, inner: RuleSchema, weight: float) -> None:
        self._inner = inner
        self._weight = weight

    def __call__(self, system: CategorySystem) -> RuleSystem:
        rs = self._inner(system)
        binary_w = tuple(self._weight for _ in rs.binary_rules) or None
        unary_w = tuple(self._weight for _ in rs.unary_rules) or None

        return RuleSystem(
            binary_rules=rs.binary_rules,
            unary_rules=rs.unary_rules,
            n_categories=rs.n_categories,
            description=rs.description,
            binary_weights=binary_w,
            unary_weights=unary_w,
        )


