class ForwardApplication(BinaryRuleSchema):
    """C/B ⊗ B → C (right evaluation / forward application)."""

    @property
    def _description(self) -> str:
        return ">"

    def match(self, left, right):
        if (
            isinstance(left, SlashCategory)
            and left.direction == "/"
            and left.argument == right
        ):
            return left.result

        return None


class BackwardApplication(BinaryRuleSchema):
    """A ⊗ A\\C → C (left evaluation / backward application)."""

    @property
    def _description(self) -> str:
        return "<"

    def match(self, left, right):
        if (
            isinstance(right, SlashCategory)
            and right.direction == "\\"
            and right.argument == left
        ):
            return right.result

        return None


class ForwardComposition(BinaryRuleSchema):
    """C/B ⊗ B/A → C/A (right harmonic composition)."""

    @property
    def _description(self) -> str:
        return ">B"

    def match(self, left, right):
        if (
            isinstance(left, SlashCategory)
            and left.direction == "/"
            and isinstance(right, SlashCategory)
            and right.direction == "/"
            and left.argument == right.result
        ):
            return SlashCategory(left.result, right.argument, "/")

        return None


