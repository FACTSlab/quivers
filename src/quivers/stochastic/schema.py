# atomic schema bases
# ================================================================

class BinaryRuleSchema(RuleSchema):
    """Schema for binary rules, defined by a match predicate.

    Subclasses implement ``match(left, right) -> result | None``.
    The schema iterates over all category pairs and collects matches.
    """

    @abstractmethod
    def match(
        self,
        left: Category,
        right: Category,
    ) -> Category | None:
        """Test whether (left, right) triggers this rule.

        Parameters
        ----------
        left : Category
            Left antecedent.
        right : Category
            Right antecedent.

        Returns
        -------
        Category or None
            Result category if the pair matches, else None.
        """
        ...

    @property
    def _description(self) -> str:
        return type(self).__name__

    def __call__(self, system: CategorySystem) -> RuleSystem:
        rules: list[tuple[int, int, int]] = []
        cats = list(system)

        for i, left in enumerate(cats):
            for j, right in enumerate(cats):
                result = self.match(left, right)

                if result is not None and result in system:
                    rules.append((system.index(result), i, j))

        return RuleSystem(
            binary_rules=tuple(rules),
            n_categories=system.size,
            description=self._description,
        )

    def __repr__(self) -> str:
        return self._description


class UnaryRuleSchema(RuleSchema):
    """Schema for unary rules, defined by a match predicate.

    Subclasses implement ``match(cat, system) -> list[result]``.
    The schema iterates over all categories and collects matches.
    """

    @abstractmethod
    def match(
        self,
        cat: Category,
        system: CategorySystem,
    ) -> list[Category]:
        """Find all valid results for a given input category.

        Parameters
        ----------
        cat : Category
            Input category.
        system : CategorySystem
            The full category inventory (needed for some schemas
            that must check existence of intermediate categories).

        Returns
        -------
        list[Category]
            Valid result categories.
        """
        ...

    @property
    def _description(self) -> str:
        return type(self).__name__

    def __call__(self, system: CategorySystem) -> RuleSystem:
        rules: list[tuple[int, int]] = []
        cats = list(system)

        for i, cat in enumerate(cats):
            for result in self.match(cat, system):
                if result in system:
                    rules.append((system.index(result), i))

        return RuleSystem(
            unary_rules=tuple(rules),
            n_categories=system.size,
            description=self._description,
        )

    def __repr__(self) -> str:
        return self._description


