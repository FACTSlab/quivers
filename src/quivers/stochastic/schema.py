class LeftUnitElimination(BinaryRuleSchema):
    """I ⊗ A → A (left unitor)."""

    @property
    def _description(self) -> str:
        return "λ"

    def match(self, left, right):
        if isinstance(left, UnitCategory):
            return right

        return None


class RightUnitElimination(BinaryRuleSchema):
    """A ⊗ I → A (right unitor)."""

    @property
    def _description(self) -> str:
        return "ρ"

    def match(self, left, right):
        if isinstance(right, UnitCategory):
            return left

        return None


class ModalApplication(BinaryRuleSchema):
    """◇(C/B) ⊗ ◇B → ◇C (modal function application).

    Parameters
    ----------
    modality : str
        The modality symbol (e.g. "◇", "□").
    """

    def __init__(self, modality: str = "◇") -> None:
        self._modality = modality

    @property
    def _description(self) -> str:
        return f"{self._modality}-app"

    def match(self, left, right):
        if (
            not isinstance(left, ModalCategory)
            or left.modality != self._modality
            or not isinstance(right, ModalCategory)
            or right.modality != self._modality
        ):
            return None

        inner_left = left.inner
        inner_right = right.inner

        # ◇(C/B) ⊗ ◇B → ◇C
        if (
            isinstance(inner_left, SlashCategory)
            and inner_left.direction == "/"
            and inner_left.argument == inner_right
        ):
            return ModalCategory(self._modality, inner_left.result)

        # ◇A ⊗ ◇(A\C) → ◇C
        if (
            isinstance(inner_right, SlashCategory)
            and inner_right.direction == "\\"
            and inner_right.argument == inner_left
        ):
            return ModalCategory(self._modality, inner_right.result)

        return None


