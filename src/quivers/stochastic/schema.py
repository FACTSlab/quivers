# ================================================================
# generalized composition (special: recursive match)
# ================================================================

class GeneralizedComposition(RuleSchema):
    """Generalized composition through nested slashes (B^n).

    C/B ⊗ (B|₁Z₁)|₂Z₂...|ₙZₙ → (C|₁Z₁)|₂Z₂...|ₙZₙ

    This is a special schema because the match recurses into
    nested slash structure. At depth 1 it is ordinary harmonic
    composition; this schema only generates rules for depth >= 2.

    Parameters
    ----------
    max_depth : int
        Maximum nesting depth to traverse.
    """

    def __init__(self, max_depth: int = 2) -> None:
        self._max_depth = max_depth

    def __call__(self, system: CategorySystem) -> RuleSystem:
        rules: list[tuple[int, int, int]] = []
        cats = list(system)

        for i, left in enumerate(cats):
            if not isinstance(left, SlashCategory) or left.direction != "/":
                continue

            for j, right in enumerate(cats):
                self._collect(system, rules, left, right, i, j, depth=2)

        return RuleSystem(
            binary_rules=tuple(rules),
            n_categories=system.size,
            description=f"gen-comp(depth={self._max_depth})",
        )

    def _collect(self, system, rules, left, right, li, ri, depth):
        if depth > self._max_depth:
            return

        if not isinstance(right, SlashCategory):
            return

        if right.result == left.argument:
            result = SlashCategory(left.result, right.argument, right.direction)

            if result in system:
                rules.append((system.index(result), li, ri))

        if isinstance(right.result, SlashCategory):
            self._collect(system, rules, left, right.result, li, ri, depth + 1)

    def __repr__(self) -> str:
        return f"GeneralizedComposition(max_depth={self._max_depth})"


# ================================================================
# pattern-based schemas (from DSL rule declarations)
# ================================================================


def _match_pattern(
    pattern,
    category: Category,
    bindings: dict[str, Category],
    variables: frozenset[str],
) -> bool:
    """Match a category pattern against a concrete category.

    Attempts to unify ``pattern`` with ``category``, extending
    ``bindings`` with variable assignments. Returns True on
    success, False on failure. Mutates ``bindings`` in place.

    Parameters
    ----------
    pattern
        A category pattern AST node (CatPatternName, CatPatternSlash,
        or CatPatternProduct).
    category : Category
        The concrete category to match against.
    bindings : dict
        Current variable bindings (mutated on success).
    variables : frozenset
        The set of variable names (from the rule's parameter list).
    """
    from quivers.dsl.ast_nodes import CatPatternName, CatPatternSlash, CatPatternProduct

    if isinstance(pattern, CatPatternName):
        name = pattern.name

        if name in variables:
            # pattern variable: bind or check consistency
            if name in bindings:
                return bindings[name] == category

            bindings[name] = category
            return True

        else:
            # category atom constant: must match exactly
            return (
                isinstance(category, AtomicCategory)
                and category.name == name
            )

    elif isinstance(pattern, CatPatternSlash):
        if not isinstance(category, SlashCategory):
            return False

        if pattern.direction != category.direction:
            return False

        return (
            _match_pattern(pattern.result, category.result, bindings, variables)
            and _match_pattern(pattern.argument, category.argument, bindings, variables)
        )

    elif isinstance(pattern, CatPatternProduct):
        if not isinstance(category, ProductCategory):
            return False

        return (
            _match_pattern(pattern.left, category.left, bindings, variables)
            and _match_pattern(pattern.right, category.right, bindings, variables)
        )

    return False


def _instantiate_pattern(
    pattern,
    bindings: dict[str, Category],
    variables: frozenset[str],
) -> Category | None:
    """Instantiate a category pattern with bound variables.

    Parameters
    ----------
    pattern
        A category pattern AST node.
    bindings : dict
        Variable bindings from matching.
    variables : frozenset
        The set of variable names.

    Returns
    -------
    Category or None
        The concrete category, or None if a variable is unbound.
    """
    from quivers.dsl.ast_nodes import CatPatternName, CatPatternSlash, CatPatternProduct

    if isinstance(pattern, CatPatternName):
        if pattern.name in variables:
            return bindings.get(pattern.name)

        return AtomicCategory(pattern.name)

    elif isinstance(pattern, CatPatternSlash):
        result = _instantiate_pattern(pattern.result, bindings, variables)
        argument = _instantiate_pattern(pattern.argument, bindings, variables)

        if result is None or argument is None:
            return None

        return SlashCategory(result, argument, pattern.direction)

    elif isinstance(pattern, CatPatternProduct):
        left = _instantiate_pattern(pattern.left, bindings, variables)
        right = _instantiate_pattern(pattern.right, bindings, variables)

        if left is None or right is None:
            return None

        return ProductCategory(left, right)

    return None


class PatternBinarySchema(BinaryRuleSchema):
    """Binary rule schema from a DSL rule declaration.

    Matches category pairs against two premise patterns and
    instantiates the conclusion pattern with bound variables.

    Parameters
    ----------
    left_pattern
        Category pattern for the left antecedent.
    right_pattern
        Category pattern for the right antecedent.
    conclusion_pattern
        Category pattern for the consequent.
    variables : frozenset of str
        Universally quantified pattern variables.
    name : str
        The rule's name (for description).
    """

    def __init__(
        self,
        left_pattern,
        right_pattern,
        conclusion_pattern,
        variables: frozenset[str],
        name: str = "",
    ) -> None:
        self._left = left_pattern
        self._right = right_pattern
        self._conclusion = conclusion_pattern
        self._variables = variables
        self._name = name

    @property
    def _description(self) -> str:
        return self._name or "pattern-binary"

    def match(self, left: Category, right: Category) -> Category | None:
        bindings: dict[str, Category] = {}

        if not _match_pattern(self._left, left, bindings, self._variables):
            return None

        if not _match_pattern(self._right, right, bindings, self._variables):
            return None

        return _instantiate_pattern(
            self._conclusion, bindings, self._variables,
        )


class PatternUnarySchema(UnaryRuleSchema):
    """Unary rule schema from a DSL rule declaration.

    Matches a single category against a premise pattern and
    instantiates the conclusion pattern with bound variables.

    Parameters
    ----------
    premise_pattern
        Category pattern for the antecedent.
    conclusion_pattern
        Category pattern for the consequent.
    variables : frozenset of str
        Universally quantified pattern variables.
    name : str
        The rule's name (for description).
    """

    def __init__(
        self,
        premise_pattern,
        conclusion_pattern,
        variables: frozenset[str],
        name: str = "",
    ) -> None:
        self._premise = premise_pattern
        self._conclusion = conclusion_pattern
        self._variables = variables
        self._name = name

    @property
    def _description(self) -> str:
        return self._name or "pattern-unary"

    def match(
        self,
        cat: Category,
        system: CategorySystem,
    ) -> list[Category]:
        bindings: dict[str, Category] = {}

        if not _match_pattern(self._premise, cat, bindings, self._variables):
            return []

        result = _instantiate_pattern(
            self._conclusion, bindings, self._variables,
        )

        if result is None:
            return []

        return [result]


